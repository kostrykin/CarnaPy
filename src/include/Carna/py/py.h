// ----------------------------------------------------------------------------------
// free
// ----------------------------------------------------------------------------------

template< typename T >
void free( T* ptr )
{
    delete ptr;
}



// ----------------------------------------------------------------------------------
// DEF_FREE
// ----------------------------------------------------------------------------------

#define DEF_FREE( cls ) def( "free", &Carna::py::free< cls > )



// ----------------------------------------------------------------------------------
// float2huv
// ----------------------------------------------------------------------------------

inline short float2huv( float value )
{
    return static_cast< short >( value * (1 << 12) + 0.5 ) - 1024;
}



